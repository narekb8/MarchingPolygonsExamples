    [BurstCompile]
    private struct March : IJobParallelFor
    {
        [ReadOnly]
        public NativeArray<float> voxels;
        [ReadOnly]
        public int4 dimensions;
        [ReadOnly]
        public float isoLevel;
        [ReadOnly]
        public NativeArray<int> cornerIndexAFromEdge, cornerIndexBFromEdge, triTable;

        [NativeDisableParallelForRestriction, WriteOnly]
        public NativeArray<Vertex> vertices;
        [NativeDisableParallelForRestriction, WriteOnly]
        public NativeArray<int> indices;
        [NativeDisableParallelForRestriction]
        public NativeCounter counter;

        private int CoordToIndex(int3 coord)
        {
            return coord.z * dimensions.y * dimensions.x + coord.y * dimensions.x + coord.x;
        }

        float3 interp(int3 coordA, int3 coordB)
        {
            float diff = (isoLevel - voxels[CoordToIndex(coordA)]) / (voxels[CoordToIndex(coordB)] - voxels[CoordToIndex(coordA)]);
            float3 floatA = new float3(coordA.x, coordA.y, coordA.z);
            float3 floatB = new float3(coordB.x, coordB.y, coordB.z);

            return floatA + diff * (floatA - floatB);
        }

        float3 calcCoordNormal(int3 coord)
        {
            int3 offsetX = new int3(1, 0, 0);
            int3 offsetY = new int3(0, 1, 0);
            int3 offsetZ = new int3(0, 0, 1);

            float densityX = voxels[CoordToIndex(coord + offsetX)] - voxels[CoordToIndex(coord - offsetX)];
            float densityY = voxels[CoordToIndex(coord + offsetY)] - voxels[CoordToIndex(coord - offsetY)];
            float densityZ = voxels[CoordToIndex(coord + offsetZ)] - voxels[CoordToIndex(coord - offsetZ)];

            return math.normalizesafe(new float3(densityX, densityY, densityZ));
        }

        float3 calcVertexNormal(int3 coordA, int3 coordB)
        {
            float3 normalA = calcCoordNormal(coordA);
            float3 normalB = calcCoordNormal(coordB);
            
            float diff = (isoLevel - voxels[CoordToIndex(coordA)]) / (voxels[CoordToIndex(coordB)] - voxels[CoordToIndex(coordA)]);
            return normalA + diff * (normalB - normalA);
        }

        public void Execute(int id)
        {
            int3 id3 = new int3(id % dimensions.x, id / dimensions.x % dimensions.y, id / (dimensions.x * dimensions.y));
            if (id3.x >= dimensions.x - 1 || id3.y >= dimensions.y - 1 || id3.z >= dimensions.z - 1)
            {
                return;
            }

            NativeArray<int3> _CubePoints = new NativeArray<int3>(8, Allocator.Temp);
            _CubePoints[0] = id3;
            _CubePoints[1] = id3 + new int3(1, 0, 0);
            _CubePoints[2] = id3 + new int3(1, 0, 1);
            _CubePoints[3] = id3 + new int3(0, 0, 1);
            _CubePoints[4] = id3 + new int3(0, 1, 0);
            _CubePoints[5] = id3 + new int3(1, 1, 0);
            _CubePoints[6] = id3 + new int3(1, 1, 1);
            _CubePoints[7] = id3 + new int3(0, 1, 1);

            int _CubeConfig = 0;
            for (int i = 0; i < 8; i++)
            {
                if(voxels[CoordToIndex(_CubePoints[i])] > isoLevel)
                {
                    _CubeConfig |= 1 << i;
                }
            }

            for(int i = 0; i < 16; i += 3)
            {
                if (triTable[_CubeConfig * i] == -1) { break; }

                int _EdgeA = triTable[(_CubeConfig * 16) + i];
                int a0 = cornerIndexAFromEdge[_EdgeA];
                int a1 = cornerIndexBFromEdge[_EdgeA];

                int _EdgeB = triTable[(_CubeConfig * 16) + i + 1];
                int b0 = cornerIndexAFromEdge[_EdgeB];
                int b1 = cornerIndexBFromEdge[_EdgeB];

                int _EdgeC = triTable[(_CubeConfig * 16) + i + 2];
                int c0 = cornerIndexAFromEdge[_EdgeC];
                int c1 = cornerIndexBFromEdge[_EdgeC];

                Vertex v1, v2, v3;
                v1.vertex = interp(_CubePoints[a0], _CubePoints[a1]);
                v1.normal = calcVertexNormal(_CubePoints[a0], _CubePoints[a1]);

                v2.vertex = interp(_CubePoints[b0], _CubePoints[b1]);
                v2.normal = calcVertexNormal(_CubePoints[b0], _CubePoints[b1]);

                v3.vertex = interp(_CubePoints[c0], _CubePoints[c1]);
                v3.normal = calcVertexNormal(_CubePoints[c0], _CubePoints[c1]);
                
                int triCount = counter.Count;
                counter.Increment();

                vertices[triCount * 3] = v1;
                vertices[triCount * 3 + 1] = v2;
                vertices[triCount * 3 + 2] = v3;

                indices[triCount * 3] = triCount * 3;
                indices[triCount * 3 + 1] = triCount * 3 + 1;
                indices[triCount * 3 + 2] = triCount * 3 + 2;
            }
        }
    }
