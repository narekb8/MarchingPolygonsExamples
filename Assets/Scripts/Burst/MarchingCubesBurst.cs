    Mesh ConstructMesh()
    {
        CreateBuffers();

        for(int i = 0; i < _Dimensions.z; i++)
        {
            for(int j = 0; j < _Dimensions.y; j++)
            {
                for(int k = 0; k < _Dimensions.x; k++)
                {
                    float val = Fbm(new Vector3(k / (float)_Dimensions.x, j / (float)_Dimensions.y, i / (float)_Dimensions.z), 1) + 0.5f;
                    _VoxelBuffer[i * _Dimensions.y * _Dimensions.x + j * _Dimensions.x + k] = val;
                    //Debug.Log(val);
                }
            }
        }

        Mesh mesh = new Mesh();

        var vertexPos = new VertexAttributeDescriptor(VertexAttribute.Position, VertexAttributeFormat.Float32, 3);
        var vertexNorm = new VertexAttributeDescriptor(VertexAttribute.Normal, VertexAttributeFormat.Float32, 3);

        March meshJob = new March
        {
            voxels = _VoxelBuffer,
            dimensions = new int4(_Dimensions.x, _Dimensions.y, _Dimensions.z, 0),
            isoLevel = _IsoLevel,
            cornerIndexAFromEdge = _CornerIndexAFromEdge,
            cornerIndexBFromEdge = _CornerIndexBFromEdge,
            triTable = _TriTable,
            vertices = _VertexBuffer,
            indices = _IndexBuffer,
            counter = _Counter
        };

        JobHandle handle = meshJob.Schedule(_Dimensions.x * _Dimensions.y * _Dimensions.z, 1);

        handle.Complete();

        for(int i = 0; i < 1000; i++)
        {
            Debug.Log(meshJob.vertices[i].vertex);
        }

        int vertexCount = meshJob.vertices.Length;
        mesh.SetVertexBufferParams(vertexCount, vertexPos, vertexNorm);
        mesh.SetIndexBufferParams(vertexCount, IndexFormat.UInt32);
        mesh.SetVertexBufferData(_VertexBuffer.ToList(), 0, 0, vertexCount, 0, MeshUpdateFlags.Default);
        mesh.SetIndexBufferData(_IndexBuffer.ToList(), 0, 0, meshJob.indices.Length, MeshUpdateFlags.Default);

        mesh.SetSubMesh(0, new SubMeshDescriptor(0, vertexCount), MeshUpdateFlags.Default);
        
        return mesh;
    }

